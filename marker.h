#define MARKER_DIFF_LONG		(700.0f)	// 敵までの距離(遠い)
	
//*****************************************************************************
// マーカーの構造体定義
//*****************************************************************************
typedef struct
{
	D3DXVECTOR3 pos;		// 位置
	bool bUse;				// 使用状態
}Marker;

